public class Relational {
    public static void main(String[] args) {
       int A = 10;
       int B = 34;
       System.out.println(A==B); // boolean answer
       System.out.println(A!=B);
       System.out.println((A > B) + " " + (A < B));
       A = 45;
       System.out.println((A>=B)+" "+(A<=B));
    }
}